public class Point {

    public Point(int X, int Y) {
        this.X = X;
        this.Y = Y;
    }
    private int X;
    private int Y;

    public int getX() {
        return X;
    }

    public void setX(int X) {
        this.X = X;
    }

    public int getY() {
        return Y;
    }

    public void setY(int Y) {
        this.Y = Y;
    }
}
